export const getUniqueCategories = () => {
  return Array.from(new Set(products.map(p => p.category)));
};

export const getUniqueTypes = () => {
  return Array.from(new Set(products.flatMap(p => p.type)));
};

export const getUniqueFlushes = () => {
  return Array.from(new Set(products.map(p => p.flush)));
};

export const getUniqueRegions = () => {
  return Array.from(new Set(products.map(p => p.region)));
};

export const getUniquePackaging = () => {
  return Array.from(new Set(products.map(p => p.packaging)));
};
